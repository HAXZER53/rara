/** Базовый интерфейс задания */
interface NPCTaskItemBase {
    /** Название */
    name: string,
    /** Описание */
    desc: string,
    /** Награда */
    reward: number,
    /** КД на повторное взятие данного задания в минутах */
    cooldown: number,
    /** Сколько точек брать для выполнения задания из массива заданий */
    count: number,
    /** Если указать true - то награда будет получена только у NPC, вернувшись к нему */
    returnToNpc: boolean,
}

export interface NPCTaskItemVehicle extends NPCTaskItemBase {
    type: "vehicle",
    /** Модели ТС, которые необходимо эвакуировать */
    models: string[],
    /** Куда доставить ТС, если не указать - в меню на G будет пункт эвакуировать и больше ничего, если указать - то нужно будет вскрыть ТС отмычками */
    returnPoint?: {x: number, y: number, z: number, name: string, type: number, r: number},
    /** Места спавна транспорта */
    positions: {x: number, y: number, z: number, h: number}[]
}


